<p>There are three functions to allocate memory in C. They are as follows:</p>
<p>calloc(): This function allocates memory and initializes this memory block to zero. It returns a pointer to the block of memory.</p>
<p>malloc(): This function reserves a memory area, and it returns a pointer of void type.</p>
