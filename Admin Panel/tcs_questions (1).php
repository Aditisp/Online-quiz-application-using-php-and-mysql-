<p>There are five types of inheritance in C++ programming. They are 1) Single inheritance, 2) Multiple Inheritance, 3) Multi level Inheritance, 4) Hybrid Inheritance, and 5) Hierarchical Inheritance.</p><br><br>

<h3>25) Why normalizes a database?</h3>
<p>A database should be normalized so that you can organize a database efficiently. You should also normalize a database to remove redundant data.</p><br><br>

<h3>26) What is DBMS?</h3>
<p>DBMS stands for Database Management System. It is a software used to manage your database. DBMS provides an interface between application and databases.</p><br><br>

