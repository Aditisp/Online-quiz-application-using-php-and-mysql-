<p>identified using their parameters.</p><br><br>


<h3>7) Explain two integrity rules used in DBMS.</h3>
<p>Referential integrity rules and Entity integrity rules</p>
<p>Referential integrity rule states that the database does not contain orphan record foreign key values. In this case, the primary key value cannot be modified if you have used this value as a foreign key in the child table.</p>
<p>In entity integrity rules, you cannot keep the primary key value null.</p><br><br>

