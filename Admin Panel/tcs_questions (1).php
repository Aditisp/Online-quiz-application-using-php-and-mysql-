<p>A data warehouse is a business intelligence tool that stores data from operational databases as well as from external sources. It supports decision-making process in an organization.</p>
<p>We should use a data warehouse to ensure consistency of collected data. It also helps business leaders to develop data-driven strategies.</p><br><br>

