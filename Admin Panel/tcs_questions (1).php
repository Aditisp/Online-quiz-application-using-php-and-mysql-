<h3>3) Name four storage classes available in C.</h3>
<p>Four storage classes available in C languages are 1) register, 2) auto, 3) extern, and 4) static.</p><br><br>

<h3>4) Explain static variable.</h3>
<p>It is an access specifier. The value of a static variable does not change during the execution of the program.</p><br><br>

<h3>5) How to print an address?</h3>
