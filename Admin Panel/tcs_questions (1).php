<h3>17) What do you mean by data abstraction? Explain three levels of data abstractions.</h3>
<p>Data abstraction is a process of recognizing the characteristics of an object or any situation and filtering out</p>
<p>unwanted characteristic.</p>
<p>Following are three levels of data abstraction:</p>
<p>Physical level: Depicts how you will store data in the database</p>
<p>Logical level: Depicts what data is sorted in the database</p>
<p>View level: End-user work on this level, and if any changes found, they will be saved by another name.</p><br><br>

<h3>18) Explain what command-line argument in C is?</h3>
<p>It is a process of getting arguments from the command prompt in C programming. There are three arguments in</p>
<p>C main function. Argument counter, Argument vector, and environment vector.</p><br><br>

<h3>19) Explain the concept of cache memory.</h3>
<p>A cache is a small size computer memory that is used by CPU to reduce average cost to access data from main memory.</p><br><br>

<h3>20) Explain the difference between pass by reference and pass by value.</h3>
<p>In pass by value, we pass the value to calling function. In Pass by reference, we need to pass the address to the caller function instead of passing the value.</p><br><br>

<h3>21) What is a pointer?</h3>
<p>A variable which stores the address of value located in memory is called pointer.</p><br><br>

<h3>22) Differentiate between null and void pointer.</h3>
<p>A null pointer is a pointer which points to nowhere. It has the value zero. Void pointer is a generic pointer which is introduced by ANSI (American National Standards Institute).</p><br><br>

<h3>23) What is operator overloading?</h3>
<p>Operator overloading is a polymorphism which allows existing operators to be redefined so that they can be used on objects of user-defined classes.</p><br><br>

